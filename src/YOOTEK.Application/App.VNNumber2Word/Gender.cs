namespace NumToWords.VietNamese
{
    /// <summary>
    /// Represents noun gender in VietNamese language.
    /// </summary>
    public enum Gender
    {
        /// <summary>
        /// Masculine noun gender. Default gender for number without any unit of measure.
        /// </summary>
        Masculine,

        /// <summary>
        /// Feminine noun gender.
        /// </summary>
        Feminine,

        /// <summary>
        /// Neuter noun gender.
        /// </summary>
        Neuter
    }
}
